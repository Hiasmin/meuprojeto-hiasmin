    private void lancarperguntaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lancarperguntaActionPerformed
        Integer idsorteio = (int) (Math.random() * lista.size());
        for (Pergunta p : lista) {
            if (p.getId() == idsorteio) {
                txtEnunciado.setText(p.getEnunciado());
                label1.setText(p.getA());
                label2.setText(p.getB());
                label3.setText(p.getC());
                label4.setText(p.getD());
            } 
        }
    }//GEN-LAST:event_lancarperguntaActionPerformed

